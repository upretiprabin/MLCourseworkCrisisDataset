# This empty file marks the `src/` directory as a Python *package*.
# A package is just a folder Python treats as importable — once this file exists
# we can write `from src.utils import RANDOM_STATE` instead of fiddling with
# sys.path tricks.
#
# Beginner note: in older Python (2.x) this file had to exist for a folder to
# be importable. Modern Python (3.3+) supports "namespace packages" without it,
# but having an explicit __init__.py is still the convention because it makes
# the package boundary obvious to humans and tools.
